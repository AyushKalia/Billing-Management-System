    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        try{
            Connection con = ConnectionProvider.getCon();
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("select * from product");
            DefaultTableModel model = new DefaultTableModel();
            model.setColumnIdentifiers(new String[]{"ProductID","Product Name","Price","Description","Activate"});
            table.setModel(model);
            while(rs.next()){
                model.addRow(new String[]{rs.getString("pID"),rs.getString("pName"),rs.getString("pRate"),rs.getString("description"),rs.getString("activate")});
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null,e);
        }
    }//GEN-LAST:event_formComponentShown

